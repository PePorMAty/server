// routes/chain/utils/prompt.js

function buildChainSystemPrompt(productName = "") {
  // Можно оставить как в Python. Я добавил мягкую подсказку про Продукт1 = productName (не ломает формат).
  const tail = productName
    ? `\n\nДОПОЛНИТЕЛЬНО:\n- Продукт1 должен соответствовать целевому продукту: ${productName}.`
    : "";

  return (
    `Ты инженер-технолог. Преобразуй описание технологии в JSON-структуру графа.

ЖЕСТКИЕ ПРАВИЛА ФОРМАТА:
1) Верни ТОЛЬКО валидный JSON-объект, без текста вокруг.
2) Структура строго такая: {"Цепочка":[ ...узлы... ]}.
3) Узлы бывают двух типов:
   - Продукт: поля "Id узла","Тип узла"="Продукт","Продукты":[...],"Название узла", "Описание продукта".
   - Преобразование: поля "Id узла","Тип узла"="Преобразование","Название технологии","Описание технологии", "Входы","Выходы".
4) В "Входы" элементы строго вида { "вход 1": "Продукт2" }, { "вход 2": "Продукт3" } ...
   В "Выходы" аналогично: { "выход 1": "Продукт1" } ...
   В каждом таком объекте ДОЛЖЕН быть ровно один ключ и одно значение.
5) Все ссылки во "Входы"/"Выходы" — это Id продукта вида "ПродуктN".
6) Создай продукт-узел ДЛЯ КАЖДОГО продукта, который фигурирует во входах/выходах.
7) Нумерация:
   - Финальный (целевой) продукт обязательно "Продукт1" и должен присутствовать в цепочке.
   - Остальные продукты: "Продукт2", "Продукт3", ...
   - Преобразования: "Преобразование1", "Преобразование2", ... по ходу процесса.
8) Не добавляй лишних шагов, если их нет в тексте. Если неоднозначность — выбирай самый типичный вариант.

Цель: полный набор продуктовых и технологических узлов, связанный входами/выходами.
`.trim() + tail
  );
}

function buildChainUserContent(techText, extraInstruction = "") {
  let s = `ОПИСАНИЕ ТЕХНОЛОГИИ:\n\n${techText || ""}`.trim();
  if (extraInstruction) s += `\n\n${extraInstruction}`;
  return s;
}

module.exports = { buildChainSystemPrompt, buildChainUserContent };
