function stringifyExistingChain(chain) {
  if (chain === null || chain === undefined) return "[]";
  if (typeof chain === "string") return chain.trim() || "[]";
  try {
    return JSON.stringify(chain, null, 2);
  } catch {
    return String(chain);
  }
}

function formatBlocksForPrompt(blocks) {
  const arr = Array.isArray(blocks) ? blocks : [];
  return arr
    .map((b, i) => {
      const txt = String(b || "").trim();
      if (!txt) return null;
      const first = txt.split("\n", 1)[0].trim();
      if (/^Источник\s*\d+\s*:?\s*$/i.test(first)) return txt;
      return `Источник ${i + 1}\n\n${txt}`;
    })
    .filter(Boolean)
    .join("\n\n---\n\n");
}

function buildStepAggregatePrompts({
  productName,
  existingChain,
  blocks,
  ancestorProducts = [],
}) {
  const blocksText = formatBlocksForPrompt(blocks);
  const chainText = stringifyExistingChain(existingChain);

  const lineageList = (Array.isArray(ancestorProducts) ? ancestorProducts : [])
    .map((s) => String(s || "").trim())
    .filter(Boolean);
  const lineageText = lineageList.length
    ? lineageList.map((a) => `- ${a}`).join("\n")
    : "(пусто — это первый шаг цепочки, петля невозможна)";

  const USER_PROMPT = STEP_AGGREGATE_USER_TEMPLATE.replace(
    "<<<TARGET_PRODUCT>>>",
    String(productName || "").trim(),
  )
    .replace("<<<EXISTING_CHAIN>>>", chainText || "[]")
    .replace("<<<LINEAGE>>>", () => lineageText)
    .replace("<<<BLOCKS>>>", blocksText);

  return {
    SYSTEM: STEP_AGGREGATE_SYSTEM,
    USER_PROMPT,
    lineageText,
  };
}

